#九九乘法表是從9*9開始的先設i和j為9和9
i,j=9,9
#while迴圈(當j小於九的時候，則一直在回圈內)
while 1<=j<=9:
#另一個while迴圈(當i小於9得時候，則印出每一排的數值，每三個要換行，每結束9到1的一個輪迴要空一行
	while 1<=i<=9:
		print(i,"x",j,"=",i*j,end="\t")
		print(i,"x",j-1,"=",i*(j-1),end="\t")
		print(i,"x",j-2,"=",i*(j-2),end="\n")
		i-=1
	print()#跳一行
	i=9#i的迴圈結束後要再從9開始
	j-=3#每三個一行所以下一行要從j-3開始
	

